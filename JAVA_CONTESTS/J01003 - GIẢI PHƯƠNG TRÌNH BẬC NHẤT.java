package b144;

import java.util.Scanner;

/**
 *
 * @author HP
 */
public class PT_bac_nhat {

    public static void main(String[] args) {
        int a, b;
        Scanner sc = new Scanner(System.in);
        a = sc.nextInt();
        b = sc.nextInt();
        if (a == 0 && b == 0) {
            System.out.println("VSN");
        } else if (a == 0 && b != 0) {
            System.out.println("VN");
        } else {
            float c = (float) -b / a;
            System.out.printf("%.2f\n",c);
        }
    }
}
