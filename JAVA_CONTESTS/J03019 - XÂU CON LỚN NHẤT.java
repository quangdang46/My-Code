package String;

import java.util.Scanner;

public class Xau_Con_Lon_nhat {
    public static void main(String[] args) {
        Scanner scan=new Scanner(System.in);
        String s=scan.next();
        int x=0;
        for(int i='z';i>='a';i--){
            for(int j=x;j<s.length();j++){
                if(s.charAt(j)==i){
                    System.out.printf(String.valueOf(s.charAt(j)));
                    x=j;
                }
            }
        }
    }
}
