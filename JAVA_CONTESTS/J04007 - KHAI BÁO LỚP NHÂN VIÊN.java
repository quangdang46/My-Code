package b1;

import java.util.Scanner;

/**
 *
 * @author HP
 */
public class Khai_bao_lop_nhan_vien {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        nhanvien sn = new nhanvien();
        sn.setTen(sc.nextLine());
        sn.setGioitinh(sc.nextLine());
        sn.setNgsinh(sc.nextLine());
        sn.setDiachi(sc.nextLine());
        sn.setMathue(sc.nextLine());
        sn.setNgky(sc.nextLine());
        sn.show();
    }
}

class nhanvien {

    private String ten;
    private String gioitinh;
    private String ngsinh;
    private String diachi;
    private String mathue;
    private String ngky;

    public nhanvien() {
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public void setGioitinh(String gioitinh) {
        this.gioitinh = gioitinh;
    }

    public void setNgsinh(String ngsinh) {
        this.ngsinh = ngsinh;
    }

    public void setDiachi(String diachi) {
        this.diachi = diachi;
    }

    public void setMathue(String mathue) {
        this.mathue = mathue;
    }

    public void setNgky(String ngky) {
        this.ngky = ngky;
    }

    public void show() {
        System.out.printf("00001 %s %s %s %s %s %s", ten, gioitinh, ngsinh, diachi, mathue, ngky);
    }
}
