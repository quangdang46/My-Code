package b;

import java.util.Scanner;

/**
 *
 * @author HP
 */
public class hcn {

    public static void main(String[] args) {
        long d, r;
        Scanner sc = new Scanner(System.in);
        d = sc.nextLong();
        r = sc.nextLong();
        if (d <= 0 || r <= 0) {
            System.out.println(0);
        } else {
            System.out.println(2 * (d + r) + " " + (d * r));
        }
    }
}
