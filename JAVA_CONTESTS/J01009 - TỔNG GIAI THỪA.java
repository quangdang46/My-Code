package JavaBasic;

import java.util.Scanner;

public class TongGiaithua {
    public static void main(String[] args) {
        Scanner scan=new Scanner(System.in);
        int n= scan.nextInt();
        long res=0;
        long x=1;
        for(int i=1;i<=n;i++){
            x*=i;
            res+=x;
        }
        System.out.println(res);
    }
}
